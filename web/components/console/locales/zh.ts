type Primitive = string | number | boolean | null | undefined | symbol | bigint

type WidenLiteral<T> =
  T extends (...args: infer Arguments) => infer Result
    ? (...args: Arguments) => Result
    : T extends string
      ? string
      : T extends number
        ? number
        : T extends boolean
          ? boolean
          : T extends Primitive
            ? T
            : T extends readonly (infer Item)[]
              ? readonly WidenLiteral<Item>[]
              : { [Key in keyof T]: WidenLiteral<T[Key]> }

export type ConsoleCopy = WidenLiteral<typeof zhConsoleCopy>
